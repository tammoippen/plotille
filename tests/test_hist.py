def test_timehist_pendulum():
    day = duration(days=1)
    now = datetime(2018, 1, 16, 11, 9, 42, 100)
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    print()
    print(res)
    assert _hist == res


def test_timehist_numpy():
    day = np.timedelta64(1, 'D')
    now = np.datetime64('2018-01-16T11:09:42.0001')
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    print()
    print(res)
    assert _hist == res


def test_timehist_orig_dt():
    day = orig_datetime.timedelta(days=1)
    now = orig_datetime.datetime(2018, 1, 16, 11, 9, 42, 100)
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    print()
    print(res)
    assert _hist == res
