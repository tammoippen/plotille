@pytest.mark.skipif(not have_numpy, reason="No numpy installed.")
def test_timehist_numpy(expected_hist):
    day = np.timedelta64(1, "D")
    now = np.datetime64("2018-01-16T11:09:42.0001")
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    # print()
    # print(res)
    assert expected_hist == res


def test_timehist_pendulum(expected_hist):
    day = duration(days=1)
    now = datetime(2018, 1, 16, 11, 9, 42, 100)
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    # print()
    # print(res)
    assert expected_hist == res


def test_timehist_orig_dt(expected_hist):
    day = orig_datetime.timedelta(days=1)
    now = orig_datetime.datetime(2018, 1, 16, 11, 9, 42, 100)
    x = [now - i * day for i in range(10)]

    res = hist(x, bins=8)

    # print()
    # print(res)
    assert expected_hist == res


def test_hist_aggregated(expected_hist, expected_hist_aggregated):
    counts = [1945, 0, 0, 0, 0, 0, 10555, 798, 0, 28351, 0]
    bins = [
        float("-inf"),
        10,
        50,
        100,
        200,
        300,
        500,
        800,
        1000,
        2000,
        10000,
        float("+inf"),
    ]
    res = hist_aggregated(counts=counts, bins=bins)
    assert expected_hist_aggregated == res
