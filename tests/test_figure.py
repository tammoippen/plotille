def test_timeseries():
    fig = Figure()
    fig.with_colors = False

    day = interval(days=1)
    now = datetime(2018, 1, 16, 11, 9, 42, 100)
    x = [now - i * day for i in range(10)]
    x = list(reversed(x))
    y = [0.5, -0.5, 0.5, -0.5, 0.5, -0.5, 0.5, -0.5, 0.5, -0.5]

    fig.plot(x, y)

    print(fig.show())
    assert _timeseries == fig.show()


